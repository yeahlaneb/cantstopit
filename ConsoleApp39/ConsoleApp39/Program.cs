using System;
using System.Collections.Generic;

// Клас Планета
class Планета
{
    public string Назва { get; set; }

    public override string ToString()
    {
        return $"Planeta: {Назва}";
    }
}

// Клас Зірка
class Зірка
{
    public string Назва { get; set; }

    public override string ToString()
    {
        return $"Zirka: {Назва}";
    }
}

// Клас Супутник
class Супутник
{
    public string Назва { get; set; }

    public override string ToString()
    {
        return $"Suputnyk: {Назва}";
    }
}

// Клас Зоряна_система
class Зоряна_система
{
    private Зірка зірка;
    private List<Планета> планети = new List<Планета>();

    // Додавання планети в систему
    public void Додати_планету(Планета планета)
    {
        планети.Add(планета);
        Console.WriteLine($"Planety {планета.Назва} dodano do zoryanoi systemu.");
    }

    // Отримання кількості планет в системі
    public void Кількість_планет()
    {
        Console.WriteLine($"Kilkist planet v zoryanii systemi: {планети.Count}");
    }

    // Вивід назви зірки
    public void Вивести_назву_зірки()
    {
        Console.WriteLine($"Nazva zirku: {зірка.Назва}");
    }

    // Встановлення зірки для системи
    public void Встановити_зірку(Зірка зірка)
    {
        this.зірка = зірка;
    }
}

class Program
{
    static void Main(string[] args)
    {
        // Створення зоряної системи
        Зоряна_система сонячна_система = new Зоряна_система();

        // Створення зірки
        Зірка сонце = new Зірка { Назва = "Solntse" };
        сонячна_система.Встановити_зірку(сонце);

        // Додавання планет
        Планета земля = new Планета { Назва = "Zemlya" };
        сонячна_система.Додати_планету(земля);

        Планета марс = new Планета { Назва = "Mars" };
        сонячна_система.Додати_планету(марс);

        Планета меркурій = new Планета { Назва = "Merkuriy" };
        сонячна_система.Додати_планету(меркурій);

        Планета венера = new Планета { Назва = "Venera" };
        сонячна_система.Додати_планету(венера);

        Планета юпітер = new Планета { Назва = "Jupiter" };
        сонячна_система.Додати_планету(юпітер);

        Планета сатурн = new Планета { Назва = "Saturn" };
        сонячна_система.Додати_планету(сатурн);

        Планета уран = new Планета { Назва = "Uran" };
        сонячна_система.Додати_планету(уран);

        Планета нептун = new Планета { Назва = "Neptun" };
        сонячна_система.Додати_планету(нептун);

        // Вивід інформації про зоряну систему
        сонячна_система.Вивести_назву_зірки();
        сонячна_система.Кількість_планет();
    }
}

